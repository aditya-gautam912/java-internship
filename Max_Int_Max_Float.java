public class Max_Int_Max_Float {
    public static void main(String[] args){
        int maxInt = Integer.MAX_VALUE;
        int result = maxInt + 5;
        float maxFloat = Float.MAX_VALUE;
        float resultFloat = maxFloat + 10;

        System.out.println("Maximum integer value: " + maxInt);
        System.out.println("Result of adding 5 to maximum integer: " + result);
        System.out.println("Maximum float value: " + maxFloat);
        System.out.println("Result of adding 10 to maximum float: " + resultFloat);

    }    
}
